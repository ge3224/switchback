    [
        <<"<div data-page=\"main\">">>,
        render_nav(),
        <<"<main>">>,
        render_demo_hint(RenderCount),
        <<"<a href=\"/\" data-swbk class=\"back-link\">‚Üê Back to Home</a>"/utf8>>,
